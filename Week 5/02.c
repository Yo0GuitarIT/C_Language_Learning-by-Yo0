#include <stdio.h>
#include <stdlib.h>

int main(void) {
	int a=19,b=13;

	printf("a=%d b=%d \n",a,b);
	printf("------------------------------\n");
	printf("a>b,比較結果為 %d 值\n",a>b);
	printf("a<b,比較結果為 %d 值\n",a<b);
	printf("a>=b,比較結果為 %d 值\n",a>=b);
	printf("a<=b,比較結果為 %d 值\n",a<=b);
	printf("a==b,比較結果為 %d 值\n",a==b);
	printf("a!=b,比較結果為 %d 值\n",a!=b);
	
  return 0;
}
